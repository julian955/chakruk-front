const seccionNot = document.querySelector(".noticias__list")

for(let i = 0; i < noticias.length; i++){
    const div = document.createElement("div")
    div.className = "noticias__elemento"
    div.id =  noticias[i].title.split(" ").join("")

    const title = document.createElement("h2")
    title.className = "noticias__title"
    title.innerHTML = noticias[i].title

    const shortDesc = document.createElement("p")
    shortDesc.className = "noticias__short"
    shortDesc.innerHTML = noticias[i].shortDesc

    const longDesc = document.createElement("p")
    longDesc.className = "noticias__long"
    longDesc.id = noticias[i].title.split(" ").join("") + "-long"
    longDesc.innerHTML = noticias[i].longDesc

    const button = document.createElement("button")
    button.classList = "noticias__button"
    button.innerHTML = "LEER MÁS"
    button.onclick = () => show(noticias[i].title.split(" ").join("") + "-long")

    seccionNot.appendChild(div)
    div.appendChild(title)
    div.appendChild(shortDesc)
    div.appendChild(longDesc)
    div.appendChild(button)
}


function show(id) {
    console.log("mostrar", id);
    const p = document.getElementById(id)
    console.log(p);
    p.classList.toggle("show")
}